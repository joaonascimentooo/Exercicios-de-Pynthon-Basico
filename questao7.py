#Leia uma temperatura em graus Fahrenheit e apresente-a convertida em graus Celsius. A formula de conversão
#é: C = 5.0∗(F −32.0)/9.0, sendo C a temperatura em Celsius e F a temperatura em Fahrenheit.

Fahrenheit = float(input("Digite aqui o valor em Fahrenheit"))

Celsius = 5

Celsius = Celsius*(Fahrenheit - 32)/9

print("O valor em graus Celsius é : ", Celsius)